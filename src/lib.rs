pub fn convert(from: &str, from_conjugation: Conjugation, to_conjugation: Conjugation) -> String {
    let from_suffixes = get_suffixes(from_conjugation);
    let from_suffix = from_suffixes
        .iter()
        .find(|&suffix| from.ends_with(suffix))
        .expect("failed to find suffix.");
    let from_base = &from[0..from.len() - from_suffix.len()];

    // TODO: make a good way to select appropriate candidate?
    //       currently use first one in any ways.
    let to_suffix = get_suffixes(to_conjugation)
        .get(0)
        .expect("cannot get conjugation suffix; maybe the conjugation is None?");

    format!("{}{}", from_base, to_suffix)
}

